export type TranslationKey = keyof typeof translations.ar;

/** Helper to get an array from comma-separated translation strings (splits by pipe `|` for plan features) */
export function getArray(key: TranslationKey, lang: Lang): string[][] {
  const val = translations[lang][key] as unknown as string;
  if (!val) return [[]];
  return val.split('|').map(group => group.split(','));
}
